package decisionMakingStatement;

public class ElseIfLadder {

	public static void main(String[] args) {
 int mark = 85;
		
	if(mark>90)	
	{
		System.out.println("I am excellent");
	}
	else if(mark>60)
	{
		System.out.println("I am First Class");

	}
	else if(mark>40)	
	{
		System.out.println("I am Pass");

	}
	
	else
	{
		System.out.println("I am Fail");
	
	}
		
	}

}
