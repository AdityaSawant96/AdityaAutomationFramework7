package decisionMakingStatement;

public class SwitchBlock {

	public static void main(String[] args) {

		String city = "Sawantwadi";
		
		switch(city)
		{
		
		case "Mumbai" : System.out.println("welcome to the Hotel Taj");
		
		break;
		
		case "Pune"  :System.out.println("welcome to the ShanivarWada");
		
		break;
		
		case  "Delhi" :System.out.println("welcome to the Lalkila");
		 break;
		 
		 
		case "Sawantwadi" :System.out.println("welcome to the Rajwada");
		break;
		
		
		
		
		default: System.out.println("Entered Correct City");
		
		break;
		
		}
		
		
		
	}

}
